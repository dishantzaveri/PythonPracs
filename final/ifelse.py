# Demonstrate the following Conditional statements in Python with suitable examples.
# i) if statement
# ii) if else statement
# iii) if – elif – else statement

# i) if statement
x = 10
if x > 5:
    print("x is greater than 5")

# ii) if else statement
x = 10
if x > 5:
    print("x is greater than 5")
else:
    print("x is less than 5")

# iii) if – elif – else statement
x = 4.9
if x > 5:
    print("x is greater than 5")
elif x < 5:
    print("x is less than 5")
else:
    print("x is equal to 5")
